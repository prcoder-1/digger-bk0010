// Значения LIMIT для таймера БК-0010 (TMR_FREQ = f/128 = 3 МГц / 128 = 23437.5 Гц).
// Рассчитаны как round(TMR_FREQ / (2 * f_note)), где f_note — равномерно
// темперированный строй с A4 = 440 Гц, СДВИНУТЫЙ НА ОКТАВУ ВВЕРХ.
